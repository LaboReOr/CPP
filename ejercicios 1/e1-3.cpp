#include <iostream>
using namespace std;

int main(){
    int n1, n2;

    cout<<"Ingrese numero 1: "<<endl;
    cin>>n1;
    cout<<"Ingrese numero 2: "<<endl;
    cin>>n2;

    float r1 = n1*n2;
    float r2 = n1/n2;
    float r3 = n1+n2;
    float r4 = n1-n2;

    cout<<"Resultados: "<<endl;
    cout<<r1<<endl;
    cout<<r2<<endl;
    cout<<r3<<endl;
    cout<<r4<<endl;
    
}